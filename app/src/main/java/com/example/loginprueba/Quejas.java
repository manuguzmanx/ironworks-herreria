package com.example.loginprueba;

public class Quejas {
    String idQueja, queja;

    public Quejas(String idQueja, String queja) {
        this.idQueja = idQueja;
        this.queja = queja;
    }

    public String getIdQueja() {
        return idQueja;
    }

    public String getQueja() {
        return queja;
    }
}
